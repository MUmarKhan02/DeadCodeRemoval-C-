    // ── JavaScript ────────────────────────────────────────────────────────────

    private static string GetJs() => """
        function toggleFile(id) {
            const body = document.getElementById(id);
            const icon = document.getElementById('icon_' + id);
            if (!body) return;
            body.classList.toggle('hidden');
            icon.classList.toggle('collapsed');
        }

        function expandAll() {
            document.querySelectorAll('.file-body').forEach(el => el.classList.remove('hidden'));
            document.querySelectorAll('.collapse-icon').forEach(el => el.classList.remove('collapsed'));
        }

        function collapseAll() {
            document.querySelectorAll('.file-body').forEach(el => el.classList.add('hidden'));
            document.querySelectorAll('.collapse-icon').forEach(el => el.classList.add('collapsed'));
        }

        // Start with files-with-issues expanded, clean files collapsed
        document.addEventListener('DOMContentLoaded', function() {
            document.querySelectorAll('.file-block.file-clean .file-body').forEach(el => {
                el.classList.add('hidden');
                const id = el.id;
                const icon = document.getElementById('icon_' + id);
                if (icon) icon.classList.add('collapsed');
            });
        });
    """;
