using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;
using System.Text;

namespace DeadCodeEliminator;

/// <summary>
/// Generates a self-contained HTML report from scan results.
/// No external dependencies — all CSS and JS is inlined.
/// </summary>
public static class HtmlReporter
{
    public static void WriteReport(
        string outputPath,
        List<(string FilePath, ScanResult Result)> fileResults,
        string scannedPath,
        string version)
    {
        var sb = new StringBuilder();
        var allIssues = fileResults.SelectMany(f => f.Result.Issues).ToList();
        int totalFiles = fileResults.Count;
        int filesWithIssues = fileResults.Count(f => f.Result.Issues.Count > 0);
        int totalLines = fileResults.Sum(f => f.Result.TotalLines);
        int autoFix = allIssues.Count(i => !i.IsWarningOnly);
        int advisory = allIssues.Count(i => i.IsWarningOnly);
        string generatedAt = DateTime.Now.ToString("yyyy-MM-dd HH:mm:ss");

        sb.AppendLine("<!DOCTYPE html>");
        sb.AppendLine("<html lang=\"en\">");
        sb.AppendLine("<head>");
        sb.AppendLine("<meta charset=\"UTF-8\">");
        sb.AppendLine("<meta name=\"viewport\" content=\"width=device-width, initial-scale=1.0\">");
        sb.AppendLine($"<title>Dead Code Report — {Path.GetFileName(scannedPath)}</title>");
        sb.AppendLine("<style>");
        sb.AppendLine(GetCss());
        sb.AppendLine("</style>");
        sb.AppendLine("</head>");
        sb.AppendLine("<body>");

        // ── Header ────────────────────────────────────────────────────────────
        sb.AppendLine("<div class=\"header\">");
        sb.AppendLine("  <div class=\"header-inner\">");
        sb.AppendLine($"  <h1>🧹 Dead Code Report</h1>");
        sb.AppendLine($"  <div class=\"meta\">Generated by deadcode v{version} &nbsp;·&nbsp; {generatedAt}</div>");
        sb.AppendLine($"  <div class=\"meta\">Scanned: <code>{HtmlEncode(scannedPath)}</code></div>");
        sb.AppendLine("  </div>");
        sb.AppendLine("</div>");

        // ── Summary cards ─────────────────────────────────────────────────────
        sb.AppendLine("<div class=\"summary\">");
        sb.AppendLine(StatCard("Files Scanned", totalFiles.ToString(), "card-neutral"));
        sb.AppendLine(StatCard("Files with Issues", filesWithIssues.ToString(), filesWithIssues > 0 ? "card-warn" : "card-ok"));
        sb.AppendLine(StatCard("Total Lines", totalLines.ToString(), "card-neutral"));
        sb.AppendLine(StatCard("Total Issues", allIssues.Count.ToString(), allIssues.Count > 0 ? "card-bad" : "card-ok"));
        sb.AppendLine(StatCard("Auto-Fixable", autoFix.ToString(), autoFix > 0 ? "card-fix" : "card-ok"));
        sb.AppendLine(StatCard("Advisory", advisory.ToString(), advisory > 0 ? "card-advisory" : "card-ok"));
        sb.AppendLine("</div>");

        // ── Category breakdown bar chart ──────────────────────────────────────
        var byKind = allIssues
            .GroupBy(i => i.Kind)
            .OrderByDescending(g => g.Count())
            .ToList();

        if (byKind.Count > 0)
        {
            sb.AppendLine("<div class=\"section\">");
            sb.AppendLine("<h2>Issues by Category</h2>");
            sb.AppendLine("<div class=\"chart\">");
            int maxCount = byKind.Max(g => g.Count());
            foreach (var group in byKind)
            {
                int count = group.Count();
                int pct = maxCount > 0 ? (int)((count / (double)maxCount) * 100) : 0;
                string kindClass = KindCssClass(group.Key);
                sb.AppendLine($"<div class=\"chart-row\">");
                sb.AppendLine($"  <div class=\"chart-label\">{KindLabel(group.Key)}</div>");
                sb.AppendLine($"  <div class=\"chart-bar-wrap\">");
                sb.AppendLine($"    <div class=\"chart-bar {kindClass}\" style=\"width:{pct}%\"></div>");
                sb.AppendLine($"    <span class=\"chart-count\">{count}</span>");
                sb.AppendLine($"  </div>");
                sb.AppendLine($"</div>");
            }
            sb.AppendLine("</div>");
            sb.AppendLine("</div>");
        }

        // ── Top offending files ────────────────────────────────────────────────
        var worst = fileResults
            .Where(f => f.Result.Issues.Count > 0)
            .OrderByDescending(f => f.Result.Issues.Count)
            .Take(10)
            .ToList();

        if (worst.Count > 0)
        {
            sb.AppendLine("<div class=\"section\">");
            sb.AppendLine("<h2>Top Files by Issue Count</h2>");
            sb.AppendLine("<div class=\"chart\">");
            int maxFileCount = worst.Max(f => f.Result.Issues.Count);
            foreach (var (filePath, fileResult) in worst)
            {
                string name = Path.GetFileName(filePath);
                string rel = Path.GetDirectoryName(filePath) ?? "";
                int count = fileResult.Issues.Count;
                int pct = maxFileCount > 0 ? (int)((count / (double)maxFileCount) * 100) : 0;
                sb.AppendLine($"<div class=\"chart-row\">");
                sb.AppendLine($"  <div class=\"chart-label\" title=\"{HtmlEncode(filePath)}\">{HtmlEncode(name)}<span class=\"chart-sublabel\">{HtmlEncode(rel)}</span></div>");
                sb.AppendLine($"  <div class=\"chart-bar-wrap\">");
                sb.AppendLine($"    <div class=\"chart-bar bar-file\" style=\"width:{pct}%\"></div>");
                sb.AppendLine($"    <span class=\"chart-count\">{count}</span>");
                sb.AppendLine($"  </div>");
                sb.AppendLine($"</div>");
            }
            sb.AppendLine("</div>");
            sb.AppendLine("</div>");
        }

        // ── Per-file results ──────────────────────────────────────────────────
        sb.AppendLine("<div class=\"section\">");
        sb.AppendLine("<div class=\"files-header\">");
        sb.AppendLine("<h2>File Details</h2>");
        sb.AppendLine("<div class=\"toggle-all\">");
        sb.AppendLine("<button onclick=\"expandAll()\">Expand All</button>");
        sb.AppendLine("<button onclick=\"collapseAll()\">Collapse All</button>");
        sb.AppendLine("</div>");
        sb.AppendLine("</div>");

        foreach (var (filePath, fileResult) in fileResults.OrderByDescending(f => f.Result.Issues.Count))
        {
            string relativePath = filePath;
            try { relativePath = Path.GetRelativePath(scannedPath, filePath); } catch { }

            bool hasIssues = fileResult.Issues.Count > 0;
            string fileId = $"file_{Math.Abs(filePath.GetHashCode())}";
            string statusClass = hasIssues ? "file-has-issues" : "file-clean";
            string icon = hasIssues ? "⚠" : "✓";

            sb.AppendLine($"<div class=\"file-block {statusClass}\">");
            sb.AppendLine($"  <div class=\"file-title\" onclick=\"toggleFile('{fileId}')\">");
            sb.AppendLine($"    <span class=\"file-icon\">{icon}</span>");
            sb.AppendLine($"    <span class=\"file-name\">{HtmlEncode(relativePath)}</span>");
            if (hasIssues)
            {
                sb.AppendLine($"    <span class=\"file-badge\">{fileResult.Issues.Count} issue{(fileResult.Issues.Count != 1 ? "s" : "")}</span>");
                int fileAutoFix = fileResult.Issues.Count(i => !i.IsWarningOnly);
                int fileAdvisory = fileResult.Issues.Count(i => i.IsWarningOnly);
                if (fileAutoFix > 0)
                    sb.AppendLine($"    <span class=\"badge-fix\">{fileAutoFix} auto-fix</span>");
                if (fileAdvisory > 0)
                    sb.AppendLine($"    <span class=\"badge-advisory\">{fileAdvisory} advisory</span>");
            }
            else
            {
                sb.AppendLine($"    <span class=\"file-clean-label\">Clean</span>");
            }
            sb.AppendLine($"    <span class=\"file-lines\">{fileResult.TotalLines} lines</span>");
            sb.AppendLine($"    <span class=\"collapse-icon\" id=\"icon_{fileId}\">▼</span>");
            sb.AppendLine($"  </div>");

            sb.AppendLine($"  <div class=\"file-body\" id=\"{fileId}\">");

            if (!hasIssues)
            {
                sb.AppendLine("    <div class=\"no-issues\">✓ No dead code found in this file.</div>");
            }
            else
            {
                // Group by kind
                var grouped = fileResult.Issues
                    .GroupBy(i => i.Kind)
                    .OrderBy(g => g.Key.ToString());

                foreach (var group in grouped)
                {
                    sb.AppendLine($"    <div class=\"issue-group\">");
                    sb.AppendLine($"      <div class=\"issue-group-title\">{KindLabel(group.Key)} <span class=\"issue-group-count\">{group.Count()}</span></div>");
                    sb.AppendLine($"      <table class=\"issue-table\">");
                    sb.AppendLine($"        <thead><tr><th>Line</th><th>Description</th><th>Action</th><th>Code</th></tr></thead>");
                    sb.AppendLine($"        <tbody>");

                    foreach (var issue in group)
                    {
                        string lineRef = issue.LineStart == issue.LineEnd
                            ? $"{issue.LineStart}"
                            : $"{issue.LineStart}–{issue.LineEnd}";
                        string actionClass = issue.IsWarningOnly ? "action-advisory" : "action-fix";
                        string actionLabel = issue.IsWarningOnly ? "Advisory" : "Auto-fix";
                        string rowClass = issue.IsWarningOnly ? "row-advisory" : "row-fix";

                        sb.AppendLine($"          <tr class=\"{rowClass}\">");
                        sb.AppendLine($"            <td class=\"col-line\">{lineRef}</td>");
                        sb.AppendLine($"            <td class=\"col-desc\">{HtmlEncode(issue.Description)}</td>");
                        sb.AppendLine($"            <td><span class=\"action-badge {actionClass}\">{actionLabel}</span></td>");
                        sb.AppendLine($"            <td class=\"col-code\">");
                        if (issue.Context.Count > 0)
                        {
                            sb.AppendLine($"              <div class=\"code-block\">");
                            foreach (var ctx in issue.Context)
                            {
                                string lineClass = ctx.IsHighlighted ? "code-line highlighted" : "code-line";
                                string marker = ctx.IsHighlighted ? "▶" : " ";
                                sb.AppendLine($"                <div class=\"{lineClass}\">");
                                sb.AppendLine($"                  <span class=\"code-gutter\">{marker}{ctx.LineNumber}</span>");
                                sb.AppendLine($"                  <span class=\"code-text\">{HtmlEncode(ctx.Content)}</span>");
                                sb.AppendLine($"                </div>");
                            }
                            sb.AppendLine($"              </div>");
                        }
                        else if (!string.IsNullOrWhiteSpace(issue.LineContent))
                        {
                            sb.AppendLine($"              <code>{HtmlEncode(issue.LineContent)}</code>");
                        }
                        sb.AppendLine($"            </td>");
                        sb.AppendLine($"          </tr>");
                    }

                    sb.AppendLine($"        </tbody>");
                    sb.AppendLine($"      </table>");
                    sb.AppendLine($"    </div>");
                }
            }

            sb.AppendLine("  </div>"); // file-body
            sb.AppendLine("</div>"); // file-block
        }

        sb.AppendLine("</div>"); // section

        // ── Footer ────────────────────────────────────────────────────────────
        sb.AppendLine("<div class=\"footer\">");
        sb.AppendLine($"  deadcode v{version} &nbsp;·&nbsp; {generatedAt}");
        sb.AppendLine("</div>");

        sb.AppendLine("<script>");
        sb.AppendLine(GetJs());
        sb.AppendLine("</script>");

        sb.AppendLine("</body>");
        sb.AppendLine("</html>");

        File.WriteAllText(outputPath, sb.ToString(), Encoding.UTF8);
    }

    // ── Convenience overload for single file ──────────────────────────────────

    public static void WriteReport(
        string outputPath,
        string filePath,
        ScanResult result,
        string version)
    {
        WriteReport(
            outputPath,
            new List<(string, ScanResult)> { (filePath, result) },
            filePath,
            version);
    }

    // ── Helpers ───────────────────────────────────────────────────────────────

    private static string StatCard(string label, string value, string cssClass)
    {
        return $"<div class=\"card {cssClass}\"><div class=\"card-value\">{value}</div><div class=\"card-label\">{label}</div></div>";
    }

    private static string KindLabel(IssueKind kind) => kind switch
    {
        IssueKind.UnusedUsing => "Unused Using Directives",
        IssueKind.UnreachableCode => "Unreachable Code",
        IssueKind.UnusedVariable => "Unused Local Variables",
        IssueKind.CommentedCode => "Commented-Out Code",
        IssueKind.UnusedPrivateMethod => "Unused Private Methods",
        IssueKind.UnusedPrivateField => "Unused Private Fields",
        IssueKind.EmptyBlock => "Empty Blocks",
        IssueKind.DeadConditional => "Dead Conditionals",
        IssueKind.RedundantAssignment => "Redundant Assignments",
        _ => kind.ToString()
    };

    private static string KindCssClass(IssueKind kind) => kind switch
    {
        IssueKind.UnreachableCode => "bar-red",
        IssueKind.DeadConditional => "bar-red",
        IssueKind.UnusedUsing => "bar-blue",
        IssueKind.UnusedVariable => "bar-orange",
        IssueKind.UnusedPrivateMethod => "bar-purple",
        IssueKind.UnusedPrivateField => "bar-purple",
        IssueKind.CommentedCode => "bar-gray",
        IssueKind.RedundantAssignment => "bar-orange",
        IssueKind.EmptyBlock => "bar-gray",
        _ => "bar-blue"
    };

    private static string HtmlEncode(string text) =>
        text.Replace("&", "&amp;")
            .Replace("<", "&lt;")
            .Replace(">", "&gt;")
            .Replace("\"", "&quot;");

